public class Word {


    private int counter;
    private String word;

    public Word(int counter, String word) {
        this.counter = counter;
        this.word = word;
    }

    public int getCounter() {
        return counter;
    }

    public void setCounter(int counter) {
        this.counter = counter;
    }

    public String getWord() {
        return word;
    }

    public void setWord(String word) {
        this.word = word;
    }
}
